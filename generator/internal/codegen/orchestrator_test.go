func TestGenerate_HooksPreserved(t *testing.T) {
	cfg := &config.Config{
		Specs: map[string]config.SpecConfig{
			"v2": {Path: "minimal.yaml"},
		},
		DataSources: map[string]config.DataSourceConfig{
			"team": {
				Spec: "v2",
				Read: config.OperationRef{
					Path:   "/api/v2/team/{team_id}",
					Method: "get",
				},
			},
		},
	}

	outputDir := t.TempDir()
	configDir := "../../testdata"

	// First generation
	if err := Generate(cfg, configDir, outputDir, false, 5); err != nil {
		t.Fatalf("first Generate() error: %v", err)
	}

	// Modify hooks file with custom content
	hooksPath := filepath.Join(outputDir, "data_source_datadog_team_hooks.go")
	customContent := []byte("package fwprovider\n\n// Custom hook code\n")
	if err := os.WriteFile(hooksPath, customContent, 0644); err != nil {
		t.Fatalf("writing custom hooks: %v", err)
	}

	// Second generation
	if err := Generate(cfg, configDir, outputDir, false, 5); err != nil {
		t.Fatalf("second Generate() error: %v", err)
	}

	// Verify hooks file is unchanged
	data, err := os.ReadFile(hooksPath)
	if err != nil {
		t.Fatalf("reading hooks file: %v", err)
	}
	if string(data) != string(customContent) {
		t.Error("hooks file was overwritten during regeneration")
	}

	// Verify generated file was regenerated (exists and is valid Go)
	genPath := filepath.Join(outputDir, "data_source_datadog_team_generated.go")
	fset := token.NewFileSet()
	_, err = parser.ParseFile(fset, genPath, nil, parser.AllErrors)
	if err != nil {
		t.Fatalf("regenerated file is not valid Go: %v", err)
	}
}

