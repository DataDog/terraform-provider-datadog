func TestGenerate_TeamDataSource(t *testing.T) {
	cfg := &config.Config{
		Specs: map[string]config.SpecConfig{
			"v2": {Path: "minimal.yaml"},
		},
		DataSources: map[string]config.DataSourceConfig{
			"team": {
				Spec: "v2",
				Read: config.OperationRef{
					Path:   "/api/v2/team/{team_id}",
					Method: "get",
				},
			},
		},
	}

	outputDir := t.TempDir()
	configDir := "../../testdata"

	err := Generate(cfg, configDir, outputDir, false, 5)
	if err != nil {
		t.Fatalf("Generate() error: %v", err)
	}

	// Verify generated file exists
	genPath := filepath.Join(outputDir, "data_source_datadog_team_generated.go")
	if _, err := os.Stat(genPath); os.IsNotExist(err) {
		t.Fatal("generated file not created")
	}

	// Verify hooks file exists
	hooksPath := filepath.Join(outputDir, "data_source_datadog_team_hooks.go")
	if _, err := os.Stat(hooksPath); os.IsNotExist(err) {
		t.Fatal("hooks file not created")
	}

	// Verify generated file is valid Go
	fset := token.NewFileSet()
	_, err = parser.ParseFile(fset, genPath, nil, parser.AllErrors)
	if err != nil {
		t.Fatalf("generated file is not valid Go: %v", err)
	}

	// Verify generated file contains expected patterns
	content, err := os.ReadFile(genPath)
	if err != nil {
		t.Fatalf("reading generated file: %v", err)
	}

	expectedPatterns := []string{
		"// Code generated by dd-tf-generator; DO NOT EDIT.",
		"package fwprovider",
		"datadogTeamDataSource",
		"datadogTeamDataSourceModel",
		`tfsdk:"team_id"`,
		`tfsdk:"name"`,
		"schema.StringAttribute",
		"schema.Int64Attribute",
		"schema.BoolAttribute",
		"GetTeam",
		"GetTeamsApiV2",
		"TeamsApi",
		"utils.FrameworkErrorDiag",
		"GetData()",
		"GetAttributes()",
	}

	for _, pattern := range expectedPatterns {
		if !strings.Contains(string(content), pattern) {
			t.Errorf("generated file missing expected pattern: %q", pattern)
		}
	}
}

